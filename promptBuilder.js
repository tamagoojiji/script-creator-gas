/**
 * 台本生成プロンプトを構築
 * @param {string} transcript - 音声入力テキスト
 * @param {string} templateType - 'prep' or 'aida'
 * @param {string[]} targets - 投稿先（リール, ストーリーズ, Threads, X）
 * @returns {string} 完成したプロンプト
 */
function buildPrompt(transcript, templateType, targets) {
  var template = CONFIG.TEMPLATES[templateType];
  if (!template) {
    throw new Error('未対応のテンプレート: ' + templateType);
  }

  var targetStr = targets.join('・');

  var prompt = [
    '# 指示',
    'あなたはInstagramリール台本の作成AIです。',
    '以下のルールに厳密に従い、ユーザーの音声メモからリール台本を生成してください。',
    '',
    '# 投稿先',
    targetStr,
    '',
    '# テンプレート: ' + template.name,
    template.description,
    '',
    buildTemplateStructure(template),
    '',
    buildVoiceRules(),
    '',
    buildPersonalityRules(),
    '',
    buildOutputFormat(templateType),
    '',
    '# ユーザーの音声メモ（これを元に台本を作成）',
    '```',
    transcript,
    '```',
    '',
    '上記の音声メモの内容を活かしつつ、テンプレート構成に沿った台本を生成してください。',
    '必ず ```json ブロックでJSON形式のみ出力してください。説明文は不要です。'
  ].join('\n');

  return prompt;
}

/**
 * テンプレート構造の説明を生成
 */
function buildTemplateStructure(template) {
  var lines = ['# テンプレート構成（' + template.sceneCount + '）'];

  for (var i = 0; i < template.structure.length; i++) {
    var scene = template.structure[i];
    lines.push('- シーン' + (i + 1) + ': ' + scene.role + '（個性心理学: ' + scene.personality + '、推奨表情: ' + scene.expression + '）');
  }

  lines.push('- CTA: 行動を促す（表情: bow）');
  lines.push('');
  lines.push('※ シーン数は内容に応じて増減OK。ただし最低' + template.sceneCount.split('-')[0] + 'シーン。');

  if (template.note) {
    lines.push('');
    lines.push('※ ' + template.note);
  }

  return lines.join('\n');
}

/**
 * EMPATHY型用：感情引き出し質問を生成するプロンプト
 * @param {string} transcript - ユーザーの音声メモ
 * @param {string[]} targets - 投稿先
 * @returns {string} プロンプト
 */
function buildQuestionsPrompt(transcript, targets) {
  var targetStr = targets.join('・');

  return [
    '# 指示',
    'あなたはInstagramリール台本の作成を補助するAIです。',
    'ユーザーがリール台本のテーマとメイン情報を入力しました。',
    'これから台本を生成しますが、その前に「感情パート」を充実させるための質問を生成してください。',
    '',
    '# 背景',
    '- 発信者: USJ攻略情報を発信する父親（tamago）',
    '- ターゲット: 子連れファミリーの母親（共働き、世帯年収1000万+、子ども5-12歳）',
    '- 投稿先: ' + targetStr,
    '- ゴール: フォロー獲得（攻略情報を出し惜しみせず提供し、信頼を得る）',
    '',
    '# ユーザーの入力（テーマ・メイン情報）',
    '```',
    transcript,
    '```',
    '',
    '# 質問生成ルール',
    '- 2〜3個の質問を生成',
    '- 質問の目的: ユーザー（発信者）の中にある感情・体験・想いを引き出す',
    '- 質問の方向性:',
    '  - 「この情報を知らずにUSJに行った家族は、どうなると思う？」系（ターゲットの痛み）',
    '  - 「この情報を知って行った家族の1日は、どう変わる？」系（ベネフィット）',
    '  - 「この情報を発信しようと思ったきっかけは？」系（発信者の想い）',
    '- テーマの内容に合わせて質問をカスタマイズすること',
    '- 質問は短く、答えやすい形にする（音声入力で回答するため）',
    '',
    '# 出力フォーマット（厳守）',
    'JSON形式で出力。以下の構造に従うこと:',
    '',
    '```json',
    '{',
    '  "questions": [',
    '    {',
    '      "id": 1,',
    '      "question": "質問文",',
    '      "purpose": "この質問で引き出したいこと（台本のどこに使うか）"',
    '    }',
    '  ]',
    '}',
    '```',
    '',
    '必ず ```json ブロックでJSON形式のみ出力してください。説明文は不要です。'
  ].join('\n');
}

/**
 * EMPATHY型用：質問回答を含めた台本生成プロンプト
 * @param {string} transcript - 音声メモ
 * @param {string[]} targets - 投稿先
 * @param {Object[]} answers - 質問と回答の配列 [{question, answer}]
 * @returns {string} プロンプト
 */
function buildEmpathyPrompt(transcript, targets, answers) {
  var template = CONFIG.TEMPLATES.empathy;
  var targetStr = targets.join('・');

  var answersBlock = answers.map(function(a, i) {
    return 'Q' + (i + 1) + ': ' + a.question + '\nA' + (i + 1) + ': ' + a.answer;
  }).join('\n\n');

  var prompt = [
    '# 指示',
    'あなたはInstagramリール台本の作成AIです。',
    '以下のルールに厳密に従い、リール台本を生成してください。',
    '',
    '# 投稿先',
    targetStr,
    '',
    '# ターゲット',
    '子連れファミリーの母親（共働き、世帯年収1000万+、子ども5-12歳）。',
    '「時間をお金で買う」価値観を持ち、事前に情報収集するが正解がわからず迷っている層。',
    'EP（エクスプレス・パス）の購入を迷っているが、超VIPではない中間所得層。',
    '',
    '# テンプレート: ' + template.name,
    template.description,
    '',
    '## EMPATHY型の構成',
    '① フック（0〜3秒）: 失敗共感。ターゲットが経験しそうな失敗を自分事として語る。煽らない。',
    '② 共感・課題提示（3〜10秒）: 「調べたけど正解がわからない」不安に寄り添う。上から教えない。',
    '③ メイン情報（10〜40秒）: 具体的な攻略情報を出し惜しみせず提供。数字・具体例を含む。',
    '④ 感情・ベネフィット（40〜50秒）: 発信者の回答を元に、情報の先にある家族の体験を語る。',
    '⑤ CTA（50〜60秒）: フォロー促進。煽らず自然に。',
    '',
    buildTemplateStructure(template),
    '',
    buildVoiceRules(),
    '',
    buildPersonalityRules(),
    '',
    '## EMPATHY型の個性心理学ルール（追加）',
    '- 各シーンのpersonalityはテーマに応じてAIが最適なタイプを1つ選んで割り当てる',
    '- ただしMoon/Earth/Sunの全3タイプが最低1回は登場すること',
    '- フックに固定タイプはない。テーマに最も合うタイプを選ぶ',
    '',
    buildOutputFormat('empathy'),
    '',
    '# ユーザーの音声メモ（テーマ・メイン情報）',
    '```',
    transcript,
    '```',
    '',
    '# 感情引き出し質問への回答',
    '以下の回答を④感情・ベネフィットパートに活かすこと:',
    '```',
    answersBlock,
    '```',
    '',
    '上記の音声メモと質問回答を活かし、EMPATHY型テンプレートに沿った台本を生成してください。',
    '情報は出し惜しみせず具体的に。煽りNG。友達に話す感じで。',
    '必ず ```json ブロックでJSON形式のみ出力してください。説明文は不要です。'
  ].join('\n');

  return prompt;
}

/**
 * tamago口調ルール
 */
function buildVoiceRules() {
  return [
    '# tamago口調ルール（厳守）',
    '- 一人称は「自分」。「僕」「私」は使わない',
    '- 完全タメ口。敬語は絶対NG（「です」「ます」「っす」は全部NG）',
    '- 語尾: 体言止め、「〜って」「〜かな」「〜やつ」「〜みたい」「〜感じ」',
    '- 語尾伸ばし（「ー」）は使わない',
    '- 断言を避け「〜かな」「〜かと」で柔らかくする',
    '- 基本は標準語。感情が入ると関西弁が出る（「ほんまそれ」「あかん」「たまらん」）',
    '- 上から教えない。友達に話す感じ',
    '',
    '## 文の長さルール（最重要）',
    '- 1シーンのtextは30〜50文字以内。絶対に60文字を超えない',
    '- 1〜2文で完結。3文以上はNG',
    '- 冗長な修飾語・繰り返しは削る',
    '- 同じ意味を言い換えて繰り返さない',
    '- リールは離脱されたら終わり。短くテンポよく',
    '',
    '## 語尾OK例',
    '「〜みたい」「〜って」「〜やつ」「〜かな」「〜感じ」「〜楽しもう」',
    '',
    '## 語尾NG例',
    '「〜みたいっすよ」「〜ですよね」「〜かと思いますね」「〜楽しみましょ」'
  ].join('\n');
}

/**
 * 個性心理学ルール
 */
function buildPersonalityRules() {
  return [
    '# 個性心理学ルール（必須）',
    '1本の台本に Moon・Earth・Sun の全3タイプに刺さる要素を必ず含める。',
    '',
    '## 人口比率（重要）',
    '- EARTH: 約40%（最大勢力）',
    '- MOON: 約35%',
    '- SUN: 約25%（最少）',
    '→ ターゲットの約75%がEARTH + MOON。SUN向けだけの発信は大多数に届かない。',
    '',
    '## Moon（共感・安心・みんなで）— 35%',
    '- 判断基準: 「信頼できるか」「みんなもそうしてるか」「難しくないか」',
    '- 行動パターン: 信頼できる人の紹介・口コミで動く。安心材料があれば行動する',
    '- 刺さるワード: 「家族みんなで」「初めてでも安心」「みんなが笑顔になれる」「思い出に残る」「○○さんも行ってた」「サポートつき」「一緒に」',
    '- 共感・仲間意識・安心感を訴求',
    '- 避けるべき: 孤立させる表現、競争を煽る表現、データだけの無機質な説明',
    '',
    '## Earth（数字・効率・お得）— 40%（最大）',
    '- 判断基準: 「具体的にどれだけ得するか」「根拠はあるか」「他と比べてどうか」',
    '- 行動パターン: 情報を集め、納得してから動く。衝動的には行動しない',
    '- 刺さるワード: 「待ち時間◯分」「◯円お得」「効率的な回り方」「◯時に行くと空いてる」「比較した結果」「実際に検証」',
    '- 具体的な数字・データ・実用的な情報を提示',
    '- 避けるべき: 曖昧な表現、根拠のない「すごい！」、感覚的な訴求だけ',
    '',
    '## Sun（限定・特別・インパクト）— 25%',
    '- 判断基準: 「面白そう」「今だけ感」「他の人が知らない」',
    '- 行動パターン: 直感で「いい！」と思ったら即行動。長い説明は読まない',
    '- 刺さるワード: 「今年だけの限定」「ここでしか体験できない」「知る人ぞ知る」「特別な体験」「新しい」「すごい」',
    '- 希少性・緊急性・特別感を訴求',
    '- 避けるべき: 長文の説明、手順の羅列、「みんなと一緒に」の同調圧力',
    '',
    '## 配置ルール',
    '- 冒頭フック: Sun要素（インパクト）またはEarth要素（数字）で注意を引く',
    '- 展開・具体例: Earth要素（数字・効率）+ Moon要素（体験・共感）で納得させる',
    '- CTA: Moon要素（安心・仲間）+ Sun要素（限定・今だけ）で行動させる',
    '',
    '## SUN偏り防止チェック',
    '台本完成後、以下を必ず確認:',
    '- MOON要素が1つ以上あるか？（安心・共感・仲間・口コミ系ワード）',
    '- EARTH要素が1つ以上あるか？（具体的な数字・データ・比較・実用情報）',
    '- SUN要素が1つ以上あるか？（限定・特別・インパクト系ワード）',
    '- 「すごい！」「やばい！」だけで終わるシーンがないか？→ 数字と安心材料をセットで入れる',
    '',
    '## じゃんけんの法則（影響の波及）',
    'SUN → MOONに自然と影響を与える → MOONがEARTHに口コミで広げる。',
    'この流れを意識し、MOONが「教えてあげたい」と思えるような有益情報を必ず含める。'
  ].join('\n');
}

/**
 * 出力フォーマット指示
 */
function buildOutputFormat(templateType) {
  return [
    '# 出力フォーマット（厳守）',
    'JSON形式で出力。以下の構造に従うこと:',
    '',
    '```json',
    '{',
    '  "title": "台本タイトル（内容を端的に表す）",',
    '  "template": "' + templateType + '",',
    '  "scenes": [',
    '    {',
    '      "text": "読み上げテキスト（自然な口語。tamago口調で）",',
    '      "display": "字幕テキスト（短く簡潔に。「、」の位置で改行される）",',
    '      "expression": "表情（' + CONFIG.EXPRESSIONS.join(' / ') + ' から選択）",',
    '      "emphasis": ["強調する", "単語リスト"],',
    '      "personality": "Moon / Earth / Sun（どのタイプに刺さるか）",',
    '      "role": "シーンの役割（フック/問題提起/共感/解決策/CTA等）"',
    '    }',
    '  ],',
    '  "cta": {',
    '    "text": "CTA読み上げテキスト",',
    '    "expression": "bow"',
    '  },',
    '  "hashtags": ["#USJ", "#ユニバ", "...（3-4個）"],',
    '  "caption": "投稿キャプション（使い方・手順・補足情報を含めてOK。実用的に）"',
    '}',
    '```',
    '',
    '## 注意',
    '- text: TTSが読み上げるテキスト。自然な口語で。30〜50文字以内。',
    '- display: 画面に表示する字幕。textより短くする。要点だけ。「、」で改行される。',
    '- expression: キャラクターの表情。シーンの感情に合わせる。',
    '- emphasis: 字幕で強調表示する単語。1-3個。',
    '- personality: Moon/Earth/Sunのどれに刺さるシーンか明記。',
    '- 全シーンでMoon/Earth/Sunが最低1回ずつ登場すること。'
  ].join('\n');
}
